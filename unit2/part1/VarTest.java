package unit2.part1;

public class VarTest {
    public static void main(String[] args) {
        // 变量
        int a = 10; //声明+赋值
        int b;
        b = 11;

        System.out.println(a);
        System.out.println(b);


        //常量
        final int d = 10;
        final int e;
        e = 11 ;
        a = 12;
        System.out.println(a);
    }
}
